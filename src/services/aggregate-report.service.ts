function buildAggregateHtml(title: string, subtitle: string, bodyContent: string, digitalSignature?: string, qrCodeDataUri?: string) {
  return `<!doctype html>
<html>
  <head>
    <meta charset="utf-8" />
    <title>${escapeHtml(title)}</title>
    <style>${baseStyles}</style>
  </head>
  <body>
    <div class="page">
      <div class="header">
        <div class="title">${escapeHtml(title)}</div>
        <div class="subtitle">${escapeHtml(subtitle)}</div>
      </div>
      
      ${bodyContent}
      
      <div class="footer">
        <div>
          Generated by the Forensic Medicine Digital Platform on ${escapeHtml(new Date().toLocaleString('en-GB'))}<br/>
          <small>Signature Hash: ${escapeHtml(digitalSignature || 'N/A')}</small>
        </div>
        ${qrCodeDataUri ? `<div style="text-align: right;"><img src="${qrCodeDataUri}" width="80" height="80" alt="QR Code" /></div>` : ''}
      </div>
    </div>
  </body>
</html>`;
}

async function generatePdf(html: string, reportNamePrefix: string) {
  const browser = await puppeteer.launch({
    headless: true,
    args: ['--no-sandbox', '--disable-setuid-sandbox']
  });

  const reportDir = path.resolve(process.cwd(), 'data', 'uploads', 'aggregate_reports');
  await fs.mkdir(reportDir, { recursive: true });
  
  const filename = `${reportNamePrefix}-${uuidv4()}.pdf`;
  const filePath = path.join(reportDir, filename);

  try {
    const page = await browser.newPage();
    await page.setContent(html, { waitUntil: 'load' });
    const pdfBuffer = await page.pdf({
      format: 'A4',
      printBackground: true,
      margin: { top: '12mm', bottom: '12mm', left: '12mm', right: '12mm' }
    });
    
    await fs.writeFile(filePath, pdfBuffer);
    return { pdfBuffer, filePath, filename };
  } finally {
    await browser.close();
  }
}

function generateSignatureAndQrCode(reportType: string) {
  const timestamp = Date.now();
  const rawSignature = `aggregate-${reportType}-${timestamp}-${process.env.JWT_SECRET || 'secret'}`;
  const digitalSignature = crypto.createHash('sha256').update(rawSignature).digest('hex');
  const verificationUrl = `${process.env.PUBLIC_URL || 'http://localhost:4000'}/verify?hash=${digitalSignature}`;
  return { digitalSignature, verificationUrl };
}

// 1. Daily Case Report
export async function generateDailyCaseReportPdf(dateStr?: string) {
  const targetDate = dateStr ? new Date(dateStr) : new Date();
  
  const startOfDay = new Date(targetDate);
  startOfDay.setHours(0, 0, 0, 0);
  
  const endOfDay = new Date(targetDate);
  endOfDay.setHours(23, 59, 59, 999);

  const cases = await prisma.cases.findMany({
    where: {
      opened_date: {
        gte: startOfDay,
        lte: endOfDay
      },
      is_deleted: false
    },
    include: {
      case_type_lu: true,
      users: true,
      case_status_lu: true
    },
    orderBy: { case_id: 'desc' }
  });

  const bodyContent = `
    <p><strong>Date:</strong> ${escapeHtml(formatDate(targetDate))}</p>
    <p><strong>Total Cases:</strong> ${cases.length}</p>
    <table>
      <thead>
        <tr>
          <th>Case Number</th>
          <th>Type</th>
          <th>Status</th>
          <th>Assigned Doctor</th>
        </tr>
      </thead>
      <tbody>
        ${cases.length ? cases.map(c => `
          <tr>
            <td>${escapeHtml(c.case_number)}</td>
            <td>${escapeHtml(c.case_type_lu?.label)}</td>
            <td>${escapeHtml(c.status || c.case_status_lu?.label)}</td>
            <td>${escapeHtml(c.users?.first_name ? `${c.users.first_name} ${c.users.last_name}` : c.users?.username)}</td>
          </tr>
        `).join('') : '<tr><td colspan="4">No cases found for this date.</td></tr>'}
      </tbody>
    </table>
  `;

  const { digitalSignature, verificationUrl } = generateSignatureAndQrCode('daily');
  const qrCodeDataUri = await QRCode.toDataURL(verificationUrl);
  
  const html = buildAggregateHtml('Daily Case Report', 'Summary of cases opened on a specific date', bodyContent, digitalSignature, qrCodeDataUri);
  return await generatePdf(html, 'DAILY-REPORT');
}

// 2. Monthly Report
export async function generateMonthlyReportPdf(month?: number, year?: number) {
  const targetDate = new Date();
  const m = month !== undefined ? month : targetDate.getMonth() + 1;
  const y = year !== undefined ? year : targetDate.getFullYear();
  
  const startOfMonth = new Date(y, m - 1, 1);
  const endOfMonth = new Date(y, m, 0, 23, 59, 59, 999);

  const cases = await prisma.cases.findMany({
    where: {
      opened_date: {
        gte: startOfMonth,
        lte: endOfMonth
      },
      is_deleted: false
    },
    include: {
      case_type_lu: true,
      case_status_lu: true
    }
  });

  const byType: Record<string, number> = {};
  const byStatus: Record<string, number> = {};
  
  cases.forEach(c => {
    const typeLabel = c.case_type_lu?.label || 'Unknown';
    const statusLabel = c.status || c.case_status_lu?.label || 'Unknown';
    byType[typeLabel] = (byType[typeLabel] || 0) + 1;
    byStatus[statusLabel] = (byStatus[statusLabel] || 0) + 1;
  });

  const bodyContent = `
    <p><strong>Month/Year:</strong> ${m}/${y}</p>
    <p><strong>Total Cases:</strong> ${cases.length}</p>
    
    <div class="summary-grid">
      <div class="summary-item">
        <strong>Cases by Type</strong>
        <ul>
          ${Object.entries(byType).map(([t, count]) => `<li>${escapeHtml(t)}: ${count}</li>`).join('')}
        </ul>
      </div>
      <div class="summary-item">
        <strong>Cases by Status</strong>
        <ul>
          ${Object.entries(byStatus).map(([s, count]) => `<li>${escapeHtml(s)}: ${count}</li>`).join('')}
        </ul>
      </div>
    </div>
  `;

  const { digitalSignature, verificationUrl } = generateSignatureAndQrCode('monthly');
  const qrCodeDataUri = await QRCode.toDataURL(verificationUrl);
  
  const html = buildAggregateHtml('Monthly Report', 'Summary of cases opened in a specific month', bodyContent, digitalSignature, qrCodeDataUri);
  return await generatePdf(html, 'MONTHLY-REPORT');
}

