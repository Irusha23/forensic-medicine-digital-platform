// 4. Court Report
export async function generateCourtReportPdf() {
  const courtEvents = await prisma.court_event.findMany({
    include: {
      cases: {
        include: {
          case_type_lu: true
        }
      }
    },
    orderBy: { event_date: 'desc' },
    take: 100 // Limit to recent/upcoming 100 for brevity, or can add date params
  });
  
  const upcomingEvents = courtEvents.filter(e => e.event_date && e.event_date >= new Date());
  const pastEvents = courtEvents.filter(e => e.event_date && e.event_date < new Date());

  const buildRows = (events: typeof courtEvents) => events.length ? events.map(e => `
    <tr>
      <td>${escapeHtml(formatDate(e.event_date))}</td>
      <td>${escapeHtml(e.court_name)}</td>
      <td>${escapeHtml(e.cases?.case_number)} (${escapeHtml(e.cases?.case_type_lu?.label)})</td>
      <td>${escapeHtml(e.event_type)}</td>
      <td>${escapeHtml(e.outcome_or_response)}</td>
    </tr>
  `).join('') : '<tr><td colspan="5">No events found.</td></tr>';

  const bodyContent = `
    <h3>Upcoming Court Dates</h3>
    <table>
      <thead>
        <tr>
          <th>Date</th>
          <th>Court Name</th>
          <th>Case</th>
          <th>Event Type</th>
          <th>Outcome / Response</th>
        </tr>
      </thead>
      <tbody>${buildRows(upcomingEvents)}</tbody>
    </table>
    
    <h3 style="margin-top: 20px;">Past Court Outcomes</h3>
    <table>
      <thead>
        <tr>
          <th>Date</th>
          <th>Court Name</th>
          <th>Case</th>
          <th>Event Type</th>
          <th>Outcome / Response</th>
        </tr>
      </thead>
      <tbody>${buildRows(pastEvents)}</tbody>
    </table>
  `;

  const { digitalSignature, verificationUrl } = generateSignatureAndQrCode('court');
  const qrCodeDataUri = await QRCode.toDataURL(verificationUrl);
  
  const html = buildAggregateHtml('Court Report', 'Upcoming dates and past outcomes', bodyContent, digitalSignature, qrCodeDataUri);
  return await generatePdf(html, 'COURT-REPORT');
}

// 5. Statistical Report
export async function generateStatisticalReportPdf(startDateStr?: string, endDateStr?: string) {
  const whereClause: any = { is_deleted: false };
  let dateText = 'All Time';
  
  if (startDateStr && endDateStr) {
    const sd = new Date(startDateStr);
    const ed = new Date(endDateStr);
    ed.setHours(23, 59, 59, 999);
    whereClause.opened_date = { gte: sd, lte: ed };
    dateText = `${formatDate(sd)} to ${formatDate(ed)}`;
  }

  const cases = await prisma.cases.findMany({
    where: whereClause,
    include: {
      case_type_lu: true,
      case_status_lu: true,
      police_stations: true,
      autopsy_details: true,
      autopsy_case: true
    }
  });

  const total = cases.length;
  const byType: Record<string, number> = {};
  const byPoliceStation: Record<string, number> = {};
  const byMannerOfDeath: Record<string, number> = {};

  cases.forEach(c => {
    byType[c.case_type_lu?.label || 'Unknown'] = (byType[c.case_type_lu?.label || 'Unknown'] || 0) + 1;
    byPoliceStation[c.police_stations?.station_name || 'Unknown'] = (byPoliceStation[c.police_stations?.station_name || 'Unknown'] || 0) + 1;
    
    if (c.autopsy_details?.manner_of_death) {
      byMannerOfDeath[c.autopsy_details.manner_of_death] = (byMannerOfDeath[c.autopsy_details.manner_of_death] || 0) + 1;
    }
  });

  const buildList = (obj: Record<string, number>) => Object.entries(obj)
    .sort((a, b) => b[1] - a[1]) // Sort by count desc
    .map(([k, v]) => `<li>${escapeHtml(k)}: ${v} (${((v/total)*100).toFixed(1)}%)</li>`)
    .join('');

  const bodyContent = `
    <p><strong>Date Range:</strong> ${escapeHtml(dateText)}</p>
    <p><strong>Total Cases Processed:</strong> ${total}</p>
    
    <div class="summary-grid">
      <div class="summary-item">
        <strong>Breakdown by Case Type</strong>
        <ul>${buildList(byType) || '<li>No data</li>'}</ul>
      </div>
      <div class="summary-item">
        <strong>Breakdown by Police Station</strong>
        <ul>${buildList(byPoliceStation) || '<li>No data</li>'}</ul>
      </div>
      <div class="summary-item">
        <strong>Breakdown by Manner of Death (Autopsies)</strong>
        <ul>${buildList(byMannerOfDeath) || '<li>No data</li>'}</ul>
      </div>
    </div>
  `;

  const { digitalSignature, verificationUrl } = generateSignatureAndQrCode('statistical');
  const qrCodeDataUri = await QRCode.toDataURL(verificationUrl);
  
  const html = buildAggregateHtml('Statistical Report', 'Aggregated statistics of case processing', bodyContent, digitalSignature, qrCodeDataUri);
  return await generatePdf(html, 'STATISTICAL-REPORT');
}
